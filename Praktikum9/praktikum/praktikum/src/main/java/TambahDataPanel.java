    private void kirimButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kirimButtonActionPerformed
        String err = validateInput();
        if (err != null) {
            JOptionPane.showMessageDialog(this, err, "Validasi Gagal", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String nama = namaTextField.getText();
        String nim = nimTextField.getText();
        String prodi = (String) prodiComboBox.getSelectedItem();
        String jenisKelamin = lakiLakiRadioButton.isSelected() ? "Laki laki" : "Perempuan";
        boolean isActive = aktifCheckBox.isSelected();

        if (Mahasiswa.cekNim(nim)) {
            JOptionPane.showMessageDialog(this, "NIM sudah terdaftar!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Mahasiswa mahasiswa = new Mahasiswa(nama, nim, prodi, jenisKelamin, isActive);
        Mahasiswa.mahasiswas.add(mahasiswa);

        JOptionPane.showMessageDialog(this, "Sukses menambahkan data mahasiswa" + nama);
        clearForm();


    }//GEN-LAST:event_kirimButtonActionPerformed


