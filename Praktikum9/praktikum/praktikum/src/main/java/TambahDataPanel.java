    private String validateInput() {
        String nim = nimTextField.getText().trim();
        String nama = namaTextField.getText().trim();
        String prodi = (String) prodiComboBox.getSelectedItem();
        boolean jkL = lakiLakiRadioButton.isSelected();
        boolean jkP = perempuanRadioButton.isSelected();

        if (nim.isEmpty() || nama.isEmpty()) {
            return "NIM dan Nama wajib diisi.";
        }
        if (!nim.matches("\\d+")) {
            return "NIM harus berupa angka.";
        }
        if (prodi == null || prodi.isEmpty()) {
            return "Program studi belum dipilih.";
        }
        if (!jkL && !jkP) {
            return "Jenis kelamin belum dipilih.";
        }
        return null; // valid
    }

